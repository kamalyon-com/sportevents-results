import React, { useState, useMemo, useCallback, useEffect } from 'react';
import { Athlete, EventInfo, FilterState, RREventConfig, RRFieldMapping, SortConfig, WidgetConfig } from '../lib/types';
import { rrFetchList, RRRow } from '../lib/raceResultApi';

// ─── Helpers ──────────────────────────────────────────────────────────────────

function inferFormat(name: string, listName?: string): RREventConfig['format'] {
  const combined = `${name} ${listName ?? ''}`.toLowerCase();
  if (combined.includes('pareja') || combined.includes('pairs') || combined.includes('duo') || combined.includes('doble')) return 'pairs';
  if (combined.includes('equipo') || combined.includes('team'))  return 'teams';
  if (combined.includes('individual'))                            return 'individual';
  return undefined;
}

// ─── Local static cache (split files) ────────────────────────────────────────
// Generated by "npm run prefetch".
//
//   race-data-index.json        tiny index — loaded once on mount to populate dropdown
//   race-data-{eventId}.json    per-event results — loaded on demand when user searches
//
// Module-level state survives across re-renders and re-searches.

interface RaceDataEntry {
  info: EventInfo;
  rows: RRRow[];
}

interface RaceIndexFile {
  fetchedAt: string;
  events: EventInfo[];
}

// null = not yet attempted | false = file not found
let indexState: RaceIndexFile | false | null = null;

// Per-event cache: key is eventId string (e.g. "376174")
const eventCache: Record<string, RaceDataEntry | false> = {};

/** Base URL for data files — set by the embed script to its own folder, falls back to './' */
function dataBase(): string {
  return ((window as unknown) as Record<string, unknown>)['__SPORT_EVENTS_BASE_URL__'] as string || './';
}

async function getLocalIndex(): Promise<RaceIndexFile | false> {
  if (indexState !== null) return indexState;
  try {
    const resp = await fetch(`${dataBase()}race-data-index.json`);
    if (!resp.ok) { indexState = false; return false; }
    indexState = await resp.json() as RaceIndexFile;
    console.info(`[race-results] Index loaded — ${indexState.events.length} events (snapshot ${indexState.fetchedAt})`);
    return indexState;
  } catch {
    indexState = false;
    return false;
  }
}

async function getEventData(eventId: string): Promise<RaceDataEntry | false> {
  if (eventCache[eventId] !== undefined) return eventCache[eventId];
  try {
    const resp = await fetch(`${dataBase()}race-data-${eventId}.json`);
    if (!resp.ok) { eventCache[eventId] = false; return false; }
    eventCache[eventId] = await resp.json() as RaceDataEntry;
    return eventCache[eventId];
  } catch {
    eventCache[eventId] = false;
    return false;
  }
}

// ─── RaceResult 14 API loader ─────────────────────────────────────────────────

/**
 * Auto-detect field value from a row using a list of candidate column labels.
 * Returns the first non-empty match, or '' if none found.
 */
function pickField(row: RRRow, overrideKey: string | undefined, ...candidates: string[]): string {
  const keys = overrideKey ? [overrideKey, ...candidates] : candidates;
  for (const k of keys) {
    const v = row[k];
    if (v !== null && v !== undefined && String(v).trim() !== '') return String(v);
  }
  return '';
}

/**
 * Parse a finish time string ("MM:SS" or "HH:MM:SS") into total seconds.
 * Returns Infinity for missing/invalid times (DNF → sorted last).
 */
function parseFinishTime(t: string): number {
  if (!t) return Infinity;
  const parts = t.split(':').map(Number);
  if (parts.some(isNaN)) return Infinity;
  if (parts.length === 3) return parts[0] * 3600 + parts[1] * 60 + parts[2];
  if (parts.length === 2) return parts[0] * 60 + parts[1];
  return Infinity;
}

/**
 * When a RaceResult list contains per-gender sub-rankings (AUTORANK.p resets
 * to 1 after some rows), detect the group boundaries and label each row's
 * gender: the first group → 'F', the second → 'M', any extras → undefined.
 * Returns a Map<rowIndex, gender>.
 * If there is only one group (rank never resets) the map is empty — no override.
 */
function detectGenderGroupsFromRanks(rows: RRRow[], rankKey: string): Map<number, 'F' | 'M' | undefined> {
  const map = new Map<number, 'F' | 'M' | undefined>();
  let groupIdx = 0;
  let prevRank = 0;
  for (let i = 0; i < rows.length; i++) {
    const r = parseInt(String(rows[i][rankKey] ?? '')) || 0;
    if (r > 0 && r <= prevRank) groupIdx++; // rank reset → new gender group
    prevRank = r > 0 ? r : prevRank;
    if (groupIdx > 0) { // only label if there are 2+ groups
      map.set(i, groupIdx === 1 ? 'M' : undefined);
    }
  }
  // If only one group was detected, return empty map (no override needed)
  if (groupIdx === 0) return new Map();
  // Re-walk and fill group 0 = 'F'
  groupIdx = 0; prevRank = 0;
  for (let i = 0; i < rows.length; i++) {
    const r = parseInt(String(rows[i][rankKey] ?? '')) || 0;
    if (r > 0 && r <= prevRank) groupIdx++;
    prevRank = r > 0 ? r : prevRank;
    map.set(i, groupIdx === 0 ? 'F' : groupIdx === 1 ? 'M' : undefined);
  }
  return map;
}

/**
 * Re-rank athletes by finish_time to produce a consistent overall rank.
 * Also recomputes per-gender rank within gender groups.
 * Mutates the array in-place and returns it sorted by rank_overall.
 */
function reRankAthletes(athletes: Athlete[]): Athlete[] {
  // Sort by time ascending (DNF → Infinity → last)
  athletes.sort((a, b) => parseFinishTime(a.finish_time) - parseFinishTime(b.finish_time));
  athletes.forEach((a, i) => { a.rank_overall = i + 1; });

  // Per-gender ranking
  const groups: Array<'F' | 'M' | undefined> = ['F', 'M', undefined];
  for (const g of groups) {
    let gRank = 1;
    for (const a of athletes) {
      if (a.gender === g) { a.rank_gender = gRank++; }
    }
  }
  return athletes;
}

/**
 * Map a flat RR14 JSON row to our internal Athlete interface.
 *
 * Column labels are configured per-event in RaceResult. The widget tries common
 * Spanish and English label names, and falls back gracefully if fields are missing.
 * Use `fieldMapping` in RREventConfig to override auto-detection for a specific event.
 */
function mapRowToAthlete(row: RRRow, rank: number, eventId: string, mapping: RRFieldMapping = {}): Athlete {
  const name = pickField(row, mapping.name, 'Nombre', 'Name', 'FIRSTNAME LASTNAME', 'Apellidos Nombre', 'Participant');
  const bibRaw = pickField(row, mapping.bib, 'Dorsal', 'BIB', 'Bib', 'Nº', 'Número', 'Startnr.');
  const finishTime = pickField(row, mapping.time, 'Tiempo', 'Time', 'FINISH_TIME', 'Tiempo Oficial', 'Tiempo Final', 'Neto', 'Netto', 'Brutto');
  const category = pickField(row, mapping.category, 'Modalidad', 'Contest', 'Categoría', 'Category', 'División', 'Grupo');

  // Rank: "AUTORANK.p" / "Platz" come as "1.", "2.", etc. — parseInt handles the trailing dot.
  const rankRaw = pickField(row, mapping.rank, 'AUTORANK.p', 'Rango', 'Pos.', 'Platz', 'Pos', 'Puesto', 'Rank', 'Posición');
  const rankNum = parseInt(rankRaw) || rank;

  const genderRaw = pickField(row, mapping.gender, 'Sexo', 'Gender', 'Sex', 'M/F', 'm/w').toUpperCase();
  const gender = (() => {
    if (genderRaw === 'M' || genderRaw === 'MALE' || genderRaw === 'H' || genderRaw === 'HOMBRE') return 'M' as const;
    if (genderRaw === 'F' || genderRaw === 'W' || genderRaw === 'FEMALE' || genderRaw === 'MUJER') return 'F' as const;
    // Infer from category if gender field is absent
    const cat = category.toLowerCase();
    if (cat.includes('femen') || cat.includes('women') || cat.includes('mujer') || cat.includes(' f ') || cat.endsWith(' f')) return 'F' as const;
    if (cat.includes('hombre') || cat.includes(' m ') || cat.endsWith(' m') || cat.includes('masculin') || cat.includes('men')) return 'M' as const;
    return undefined;
  })();

  const ageGroup = pickField(row, mapping.ageGroup, '_ageGroup', 'EdadGrupo', 'Grupo de Edad', 'AgeGroup', 'Grupo Edad', 'Categoría', 'Categoria', 'Kategorie', 'Edad');
  const nationality = pickField(row, mapping.nationality, 'Nación', 'Nationality', 'NAT', 'Nat.', 'País', 'Country', 'Verein').substring(0, 2).toUpperCase();

  // Map pre-fetched split times (stored as _splits by the prefetch script)
  const rawSplits = (row as Record<string, unknown>)['_splits'] as
    | Array<{ label: string; sector: string; acc: string }>
    | null
    | undefined;
  const splits = rawSplits
    ? rawSplits
        .filter((s) => s.label)
        .map((s) => ({
          station: s.label,
          time: s.acc || s.sector,
          sector: s.sector,
          rank: 0,
        }))
    : [];

  return {
    bib: bibRaw || String(rank),
    name: name || `Dorsal ${bibRaw || rank}`,
    gender,
    age_group: ageGroup || category,
    category,
    nationality,
    finish_time: finishTime,
    rank_overall: rankNum,
    rank_gender: rankNum,   // per-gender rank not always available; use overall
    rank_category: rankNum, // same
    splits,
    event_id: eventId,
  };
}

async function fetchAthletesForRREvent(
  eventCfg: RREventConfig,
  apiKey: string,
): Promise<{ athletes: Athlete[]; eventInfo: EventInfo }> {
  const id = String(eventCfg.eventId);
  const fileKey = eventCfg.fileKey ?? id;

  // ── Try per-event local file first ───────────────────────────────────────
  const entry = await getEventData(fileKey);
  let rows: RRRow[];
  let eventInfo: EventInfo;

  if (entry) {
    rows      = entry.rows;
    eventInfo = entry.info;
    console.info(`[race-results] Local hit: ${id} (${rows.length} rows)`);
  } else {
    // ── Fall back to live API ───────────────────────────────────────────────
    const server = eventCfg.server;
    rows = await rrFetchList(id, eventCfg.listName, apiKey, server, eventCfg.contest ?? 0, eventCfg.lang ?? 'es');
    eventInfo = {
      id,
      name:     eventCfg.name ?? `Event ${id}`,
      date:     eventCfg.date ?? new Date().toISOString().split('T')[0],
      location: eventCfg.location ?? '',
    };
  }

  // Filter out non-athlete rows (e.g. team total rows that have no bib/rank).
  // A valid athlete row has at least a name or bib value.
  const bibKey  = eventCfg.fieldMapping?.bib  ?? 'Dorsal';
  const nameKey = eventCfg.fieldMapping?.name ?? 'Nombre';
  // For rank: check multiple common key names because different output lists use different labels
  const POSSIBLE_BIB_KEYS  = [bibKey,  'BIB', 'Bib', 'Nº', 'Número', 'Startnr.'].filter(Boolean);
  const POSSIBLE_RANK_KEYS = [
    eventCfg.fieldMapping?.rank,
    'AUTORANK.p', 'Pos.', 'Rango', 'Platz', 'Pos', 'Puesto', 'Rank',
  ].filter(Boolean) as string[];
  const athleteRows = rows.filter((r) => {
    const hasBib  = POSSIBLE_BIB_KEYS.some(k  => r[k]  !== null && r[k]  !== undefined && r[k]  !== '');
    const hasName = r[nameKey] !== null && r[nameKey] !== undefined && String(r[nameKey] ?? '').trim() !== '';
    const hasRank = POSSIBLE_RANK_KEYS.some(k => r[k] !== null && r[k] !== undefined && r[k] !== '');
    return hasBib || (hasName && hasRank);
  });

  // Detect per-gender sub-rankings (AUTORANK.p resets) when no gender column exists
  // Use the first rank key that actually appears in the rows
  const rankKey = POSSIBLE_RANK_KEYS.find(k => rows.length > 0 && rows[0][k] !== undefined) ?? 'AUTORANK.p';
  const genderOverride = detectGenderGroupsFromRanks(athleteRows, rankKey);

  const athletes = athleteRows.map((row, i) => {
    const a = mapRowToAthlete(row, i + 1, id, eventCfg.fieldMapping);
    // Apply detected gender if not already inferred from data
    if (a.gender === undefined && genderOverride.has(i)) {
      a.gender = genderOverride.get(i);
    }
    return a;
  });

  // Re-rank all athletes by finish_time for a consistent overall + per-gender rank
  reRankAthletes(athletes);

  return { athletes, eventInfo };
}

export type Phase = 'search' | 'loading' | 'results';

interface UseRaceResultsReturn {
  phase: Phase;
  executeSearch: (eventCfg: RREventConfig, initialFilters?: Partial<FilterState>) => void;
  backToSearch: () => void;
  reload: () => void;
  athletes: Athlete[];
  activeEvent: EventInfo | null;
  filteredAthletes: Athlete[];
  error: string | null;
  filters: FilterState;
  setFilters: React.Dispatch<React.SetStateAction<FilterState>>;
  sortConfig: SortConfig;
  setSortConfig: React.Dispatch<React.SetStateAction<SortConfig>>;
  clearFilters: () => void;
  genderOptions: string[];
  categoryOptions: string[];
  ageGroupOptions: string[];
  nationalityOptions: string[];
  /** Event list derived from the local cache (race-data.json). Empty until the cache loads. */
  availableEvents: RREventConfig[];
}

const DEFAULT_FILTERS: FilterState = {
  search: '',
  eventId: '',
  gender: '',
  ageGroup: '',
  category: '',
  nationality: '',
};

export function useRaceResults(config: WidgetConfig): UseRaceResultsReturn {
  const [phase, setPhase] = useState<Phase>('search');
  const [athletes, setAthletes] = useState<Athlete[]>([]);
  const [activeEvent, setActiveEvent] = useState<EventInfo | null>(null);
  const [error, setError] = useState<string | null>(null);
  const [filters, setFilters] = useState<FilterState>(DEFAULT_FILTERS);
  const [sortConfig, setSortConfig] = useState<SortConfig>({ field: 'rank_overall', direction: 'asc' });
  const [availableEvents, setAvailableEvents] = useState<RREventConfig[]>([]);

  // Load available events from the local index once on mount
  useEffect(() => {
    getLocalIndex().then((index) => {
      if (!index) return;
      const configs: RREventConfig[] = index.events
        .map((info) => ({
          eventId:     Number(info.id),
          fileKey:     info.fileKey ?? info.id,
          listName:    info.listName ?? '',
          contest:     info.contest ?? 0,
          name:        info.name,
          date:        info.date,
          location:    info.location,
          contestName: info.contestName,
          noResults:   info.noResults,
          // Use stored format from index (set by prefetch EVENT_VARIANTS config),
          // then fall back to keyword detection from event/list names.
          format:      info.format ?? inferFormat(info.name, info.listName),
        }))
        .filter((cfg) => !config.eventPrefix || (cfg.name ?? '').toLowerCase().includes(config.eventPrefix.toLowerCase()))
        .filter((cfg) => !cfg.date || cfg.date <= new Date().toISOString().slice(0, 10)) // hide future events
        .sort((a, b) => (b.date ?? '').localeCompare(a.date ?? '')); // newest first
      setAvailableEvents(configs);
    });
  }, [config.eventPrefix]);

  // Keep a ref to the last search params so reload() can repeat it
  const lastSearchRef = React.useRef<{ eventCfg: RREventConfig; initialFilters?: Partial<FilterState> } | null>(null);

  const doLoad = useCallback(
    async (eventCfg: RREventConfig, initialFilters?: Partial<FilterState>) => {
      setPhase('loading');
      setError(null);

      // Event is registered but has no results published yet
      if (eventCfg.noResults) {
        setAthletes([]);
        setActiveEvent({
          id:          String(eventCfg.eventId),
          name:        eventCfg.name ?? '',
          date:        eventCfg.date ?? '',
          location:    eventCfg.location ?? '',
          contestName: eventCfg.contestName,
        });
        setFilters({ ...DEFAULT_FILTERS, ...initialFilters });
        setSortConfig({ field: 'rank_overall', direction: 'asc' });
        setPhase('results');
        return;
      }

      try {
        const apiKey = config.apiKey || process.env.REACT_APP_RR_API_KEY || '';
        const result = await fetchAthletesForRREvent(eventCfg, apiKey);

        setAthletes(result.athletes);
        setActiveEvent(result.eventInfo);
        // Pre-apply any initial filters from the search form
        setFilters({ ...DEFAULT_FILTERS, ...initialFilters });
        setSortConfig({ field: 'rank_overall', direction: 'asc' });
        setPhase('results');
      } catch (e) {
        setError('Error al cargar los resultados. Inténtalo de nuevo.');
        setPhase('search');
      }
    },
    [config.apiKey],
  );

  const executeSearch = useCallback(
    (eventCfg: RREventConfig, initialFilters?: Partial<FilterState>) => {
      lastSearchRef.current = { eventCfg, initialFilters };
      doLoad(eventCfg, initialFilters);
    },
    [doLoad],
  );

  const reload = useCallback(() => {
    if (lastSearchRef.current) {
      doLoad(lastSearchRef.current.eventCfg, lastSearchRef.current.initialFilters);
    }
  }, [doLoad]);

  const backToSearch = useCallback(() => {
    setPhase('search');
    setAthletes([]);
    setActiveEvent(null);
    setError(null);
    setFilters(DEFAULT_FILTERS);
  }, []);

  const clearFilters = useCallback(
    () => setFilters((f) => ({ ...DEFAULT_FILTERS, search: f.search })),
    [],
  );

  const genderOptions = useMemo(
    () => Array.from(new Set(athletes.map((a) => a.gender).filter(Boolean))).sort() as string[],
    [athletes],
  );
  const categoryOptions = useMemo(
    () => Array.from(new Set(athletes.map((a) => a.category).filter(Boolean))).sort(),
    [athletes],
  );
  const ageGroupOptions = useMemo(
    () => Array.from(new Set(athletes.map((a) => a.age_group).filter(Boolean))).sort(),
    [athletes],
  );
  const nationalityOptions = useMemo(
    () => Array.from(new Set(athletes.map((a) => a.nationality).filter(Boolean))).sort(),
    [athletes],
  );

  const filteredAthletes = useMemo(() => {
    let result = athletes;

    if (filters.gender) result = result.filter((a) => a.gender === filters.gender);
    if (filters.ageGroup) result = result.filter((a) => a.age_group === filters.ageGroup);
    if (filters.category) result = result.filter((a) => a.category === filters.category);
    if (filters.nationality) result = result.filter((a) => a.nationality === filters.nationality);

    if (filters.search.trim()) {
      const q = filters.search.trim().toLowerCase();
      result = result.filter(
        (a) =>
          a.name.toLowerCase().includes(q) ||
          a.bib.toLowerCase().includes(q) ||
          a.category.toLowerCase().includes(q),
      );
    }

    result = [...result].sort((a, b) => {
      let valA: string | number;
      let valB: string | number;
      switch (sortConfig.field) {
        case 'rank_overall': valA = a.rank_overall; valB = b.rank_overall; break;
        case 'name': valA = a.name; valB = b.name; break;
        case 'finish_time': valA = parseFinishTime(a.finish_time); valB = parseFinishTime(b.finish_time); break;
        case 'category': valA = a.category; valB = b.category; break;
        case 'bib': valA = parseInt(a.bib) || 0; valB = parseInt(b.bib) || 0; break;
        default: valA = a.rank_overall; valB = b.rank_overall;
      }
      const cmp = valA < valB ? -1 : valA > valB ? 1 : 0;
      return sortConfig.direction === 'asc' ? cmp : -cmp;
    });

    return result;
  }, [athletes, filters, sortConfig]);

  return {
    phase,
    executeSearch,
    backToSearch,
    reload,
    athletes,
    activeEvent,
    filteredAthletes,
    error,
    filters,
    setFilters,
    sortConfig,
    setSortConfig,
    clearFilters,
    genderOptions,
    categoryOptions,
    ageGroupOptions,
    nationalityOptions,
    availableEvents,
  };
}
